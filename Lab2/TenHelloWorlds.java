package com.ict9.oop;

public class TenHelloWorlds {
    public static void main(String []args) {
        System.out.println("1st Hello, World");
        System.out.println("2nd Hello, World");
        System.out.println("3rd Hello, World");
        for(int i = 4; i <= 10; i++) {
            System.out.println(i + "th Hello, World");
        }
        System.out.println();
    }
}
